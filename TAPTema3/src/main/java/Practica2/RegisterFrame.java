    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String errores = "";
        if(fieldNombre.getText().length() < 3) {
            errores += "\nEl nombre debe tener al menos 3 caracteres";
            fieldNombre.setBackground(Color.yellow);
        }
        if(fieldCorreo.getText().length() < 3) {
            errores += "\nEl correo debe tener al menos 3 caracteres";
            fieldCorreo.setBackground(Color.yellow);
        }
        if(!fieldCorreo.getText().contains("@") && !fieldCorreo.getText().contains(".")) {
            errores += "\nEl correo no es valido";
            fieldCorreo.setBackground(Color.yellow);
        }
        if(fieldPass1.getText().length() < 3) {
            errores += "\nLa contrase単a debe tener al menos 3 caracteres";
            fieldPass1.setBackground(Color.yellow);
        }
        if(!fieldPass1.getText().equals(fieldPass2.getText())) {
            errores += "\nLas contrase単as no coinciden";
            fieldPass2.setBackground(Color.yellow);
        }
        
        if(errores.isEmpty()) {
            // No hay errores
            Usuario usr = new Usuario();
            usr.setNombre_completo(fieldNombre.getText());
            usr.setCorreo(fieldCorreo.getText());
            usr.setContrasenia(fieldPass1.getText());
            usr.setEs_admin(false);
            
            registrar(usr);
        } else {
            JOptionPane.showMessageDialog(
                null,"No se puede registrar hasta corregir lo siguiente:\n"
                    + errores
            );
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void registrar(Usuario usr) {
        try {
            UsuarioService service = new UsuarioService();
            if(service.addUsr(usr)) {
                JOptionPane.showMessageDialog(null,"Registrado");
                dispose();
            } else throw new Exception();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(
                null, "Error al registrar"
            );
        }
    }
    
