    private void mostrarPorFecha(String fecha){
        panelVentas.removeAll();
        
        int size = 0;
        total = 0;
        
        for (int i = 0; i < ventas.size(); i++) {
            HistorialPanel hp = new HistorialPanel(i);
            System.out.println(ventas.get(i).getFecha().substring(0,10));
            if(fecha.equals(ventas.get(i).getFecha().substring(0,10))){
                total += ventas.get(i).getTotal();
                panelVentas.add(hp);
                size++;
            }
            

        }
        gridLayout.setRows(size);
        
        panelVentas.updateUI();
        labelTotal.setText(String.format(" TOTAL VENTAS: $%1$,.2f", total));  
        
        if(size == 0) 
            JOptionPane.showMessageDialog(null, 
                "No se encontro ventas asociadas a la fecha buscada", 
                "Sin resultados",
                JOptionPane.ERROR_MESSAGE
            );
    }
    
    private void mostrarPorNombre(String nombre){
        panelVentas.removeAll();
        
        int size = 0;
        total = 0;
        
        for (int i = 0; i < ventas.size(); i++) {
            HistorialPanel hp = new HistorialPanel(i);
            System.out.println(ventas.get(i).getProducto().getNombreProducto());
            if(nombre.equals(ventas.get(i).getProducto().getNombreProducto().toUpperCase())){
                total += ventas.get(i).getTotal();
                panelVentas.add(hp);
                size++;
            }
            
        }
        gridLayout.setRows(size);
        
        panelVentas.updateUI();
        labelTotal.setText(String.format(" TOTAL VENTA: $%1$,.2f", total));   
        
        if(size == 0) 
            JOptionPane.showMessageDialog(null, 
                "No se encontro ventas asociadas al producto buscado", 
                "Sin resultados",
                JOptionPane.ERROR_MESSAGE
            );
    }
    
    private void mostrarVentas(){
        panelVentas.removeAll();
        gridLayout.setRows(ventas.size());
        total = 0;
        
        // Version 1 sin borrar
        /*for(Tema t : materia.getTemas()) {
            TemaPanel tp = new TemaPanel(this, t);
            panelListaTemas.add(tp);
        }*/
        
        // Version 2 con borrado
        for (int i = 0; i < ventas.size(); i++) {
            HistorialPanel hp = new HistorialPanel(i);
            total += ventas.get(i).getTotal();
            panelVentas.add(hp);
        }
        panelVentas.updateUI();
        labelTotal.setText(String.format(" TOTAL VENTA: $%1$,.2f", total));   
    }
    
