    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(criterio != 3){
            switch (criterio) {
                case 1 -> {
                    if(fieldBusqueda.getText().length() < 1)
                    {
                        JOptionPane.showMessageDialog(null,
                                    "Caja de texto vacia",
                                    "Error",
                                    JOptionPane.ERROR_MESSAGE
                                );
                        break;
                    }
                    mostrarPorFecha(fieldBusqueda.getText());
                    fieldBusqueda.setText("");
                }
                case 2 -> {
                    if(fieldBusqueda.getText().length() < 1)
                    {
                        JOptionPane.showMessageDialog(null,
                                    "Caja de texto vacia",
                                    "Error",
                                    JOptionPane.ERROR_MESSAGE
                                );
                        break;
                    }
                    mostrarPorNombre(fieldBusqueda.getText().toUpperCase());
                    fieldBusqueda.setText("");
                }
                default -> System.out.println("XD");
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

