    private void fieldClaveAsignaturaFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_fieldClaveAsignaturaFocusGained
        String claveAsignatura = fieldClaveAsignatura.getText();
        if(claveAsignatura.trim().isEmpty()) {
            String nombreAsignatura = fieldNombreAsignatura.getText();
            String [] partes = nombreAsignatura.split(" ");
            String clave = " ";
            for(String parte: partes) { //For each o For mejorado
                clave += parte.substring(0,1).toUpperCase(); //Se agrega a la cadeana: +=
            }
            clave += "-";
            fieldClaveAsignatura.setText(clave);
        }                    
    }//GEN-LAST:event_fieldClaveAsignaturaFocusGained

    private void btnGuardarAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarAgregarActionPerformed
        if ( ! valida().isEmpty()) {
            return;
        }
        asignaturas.add(materia);
        
        JOptionPane.showMessageDialog(null,"Guardado");
        
        materia = new Asignatura(); // Crear una nueva para que al guardar no remplace la informacion
        btnLimpiarActionPerformed(evt); // Propagacion de eventos, uno puede disparar a otro.
        
        capturador.muestraAsignaturas();
        capturador.updateList();
    }//GEN-LAST:event_btnGuardarAgregarActionPerformed

    private void btnGuardarFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarFinalizarActionPerformed
        if ( ! valida().isEmpty()) {
            return;
        }
        
        if (asignaturas != null) {
            asignaturas.add(materia);
        }
        
        JOptionPane.showMessageDialog(null,"Guardado");
        
        capturador.muestraAsignaturas();
        capturador.updateList();
        
        dispose();
    }//GEN-LAST:event_btnGuardarFinalizarActionPerformed

