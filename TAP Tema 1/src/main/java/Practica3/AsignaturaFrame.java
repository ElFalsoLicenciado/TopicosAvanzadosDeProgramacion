    private String valida(){
        String errores = "";
        try {
            String nombre = fieldNombreAsignatura.getText();
            if(nombre.trim().isEmpty() || nombre.trim().length() < 2) {
                errores += "\nEl nombre de la asignatura no puede ser vacío";
                errores += "\nEl nombre de la materia debe ser mayor a 1 caracter";
                fieldNombreAsignatura.setBackground(Color.yellow);
            }
            
            String clave = fieldClaveAsignatura.getText();
            if(clave.trim().isEmpty() || clave.trim().length() < 3) {
                errores += "\nLa clave de la asignatura no puede ser vacía";
                errores += "\nLa clave de la materia debe ser mayor a 2 caracteres";
                fieldClaveAsignatura.setBackground(Color.yellow);
            }
            
            Double creditos = (Double)jSpinner1.getValue();
            
            if( ! errores.isEmpty()) {
                throw new Exception("Errores de captura");
            }
            
            materia.setCreditos(creditos);
            materia.setNombreAsignatura(
                    fieldNombreAsignatura.getText()
            );
            materia.setClaveAsignatura(
                    fieldClaveAsignatura.getText()
            );
            materia.setSemestre(jSlider1.getValue());
            
        } catch (NumberFormatException nfe) {
            nfe.printStackTrace();
            errores += "\nError en los creditos";
            JOptionPane.showMessageDialog(
                null,
                "Debe corregir errores:\n" + errores,
                "Error al guardar",
                JOptionPane.ERROR_MESSAGE
            );
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(
                null,
                "Debe corregir errores:\n" + errores,
                "Error al guardar",
                JOptionPane.ERROR_MESSAGE
            );
        }
        return errores;
    }
    
