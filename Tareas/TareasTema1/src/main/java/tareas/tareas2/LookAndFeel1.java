    private void setLFListing(){
        for (javax.swing.UIManager.LookAndFeelInfo info : javax.swing.UIManager.getInstalledLookAndFeels()) {
            listadoLaF.addElement(info.getName());
        }
        listadoLaF.addElement("FlatLaF");
        listadoLaF.addElement("TinyLaF");
        listadoLaF.addElement("Radiance");
        SelectorTemas.setModel(listadoLaF);
    }
    
    
    
